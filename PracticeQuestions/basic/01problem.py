# 1️⃣ Write a Python program that imports the math module and calculates the square root of a number input by the user.
import math
userinput = float(input("Enter a number to find its square root: "))
square_root = math.sqrt(userinput)
print(f"The square root of {userinput} is {square_root}.") # This line prints the result of the square root calculation.
